package frc.robot.utils.emu

/**
 * Enum class representing the motors of an elevator system.
 * This enum is used to distinguish between the left and right motors.
 */
enum class ElevatorMotor {
    /**
     * Represents the left motor of the elevator.
     */
    LEFT,

    /**
     * Represents the right motor of the elevator.
     */
    RIGHT,
}
